				/**
				 * Google API
				 */
				function getPanorama(coords) {
					const options = {
						addressControl    : false,
						fullscreenControl : false,
						showRoadLabels    : false,
						position          : coords,
					}

					const panorama = new google.maps.StreetViewPanorama(PANORAMA_ELEMENT, options);
				}

				/**
				 * Main entry point - begin the round
				 */
				beginGame();

				/**
				 * Page functions
				 */
				function beginGame() {
					CURRENT_ROUND = 0;
					populateLocations();
					hideEl(GAME_RESULTS);
					resetMap();
					beginRound();
				}

				function beginRound() {
					if (CURRENT_ROUND < TOTAL_ROUNDS) {
						// Hide guess button
						hideEl(GUESS_BUTTON);

						// Remove markers/lines etc.
						resetMap();

						// Show panorama from coords
						getPanorama(LOCATIONS[CURRENT_ROUND]);

						// Set text labels for results screen
						LABEL_CURR_ROUND.textContent   = CURRENT_ROUND + 1;
						LABEL_TOTAL_ROUNDS.textContent = TOTAL_ROUNDS;

						// Reset timer
						ROUND_BEGIN = Date.now();
					} else {
						alert("ERROR: current round exceeded total number rounds");
					}
				}

				// All rounds completed
				function endGame() {
					document.body.classList.add("finished");
				}

				// Called when user makes a guess
				function endRound() {
					hideEl(GUESS_BUTTON);

					showGuessResults();

					// Important: should only be incremented once all other functions have finished with this value
					CURRENT_ROUND++;

					if (CURRENT_ROUND === TOTAL_ROUNDS) {
						endGame();
					}
				}

				function populateLocations() {
					// Reset
					for (let i = 0; i < LOCATIONS.length; i++) LOCATIONS[i] = null;

					// Populate
					let loc;

					for (let i = 0; i < LOCATIONS.length; i++) {
						do {
							loc = getRandomLocation();
						} while (LOCATIONS.indexOf(loc) > -1);

						LOCATIONS[i] = splitLatLng(loc);
					}
				}

				// Remove location markers / lines
				function removeMapElements() {
					for (const key in MAP_ELEMENTS) {
						if (MAP_ELEMENTS[key] !== null) {
							MAP_ELEMENTS[key].remove();
							MAP_ELEMENTS[key] = null;
						}
					}
				}

				function resetMap() {
					removeMapElements();

					// Move map back into corner and hide results (done via CSS with .guessed class)
					document.body.classList.remove("guessed", "finished");

					// Reset zoom
					MAP_OBJECT.invalidateSize();
					MAP_OBJECT.setView([40, -40], 2);
				}

