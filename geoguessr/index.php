				}

				function hideEl(el) {
					if (typeof el === "string") {
						el = $(el);
					}

					el.classList.add("hide");
				}

				// https://stackoverflow.com/a/21623206/7290573
				function distanceInKm(pos1, pos2) {
					const p = Math.PI / 180;
					const c = Math.cos;
					const a = 0.5 - c((pos2.lat - pos1.lat) * p) / 2 +
									c(pos1.lat * p) * c(pos2.lat * p) *
									(1 - c((pos2.lng - pos1.lng) * p)) / 2;

					return 12742 * Math.asin(Math.sqrt(a));
				}

