				function splitLatLng(coords) {
					const [lat, lng] = coords.split(",").map(parseFloat);

					return {
						lat: lat,
						lng: lng
					}
				}

				function formatDuration(time) {
					time = Math.round(time / 1000);

					if (time < 60) {
						if (time === 1) {
							return "1 second";
						}

						return `${time} seconds`;
					}

					let mins = Math.floor(time / 60);
					let secs = time % 60;
					let amt  = mins + (mins === 1 ? " min" : " mins");

					if (secs === 0) {
						return amt;
					}

					return amt + " " + secs + (secs === 1 ? " sec" : " secs");
				}

