				function showAllGuesses() {
					// Remove any existing markers/lines
					removeMapElements();

					// Add all guesses (to a "featureGroup" so fitBounds can be called)
					MAP_ELEMENTS.allGuesses = L.featureGroup();

					for (let i = 0; i < ROUNDS_INFO.length; i++) {
						const location  = LOCATIONS[i];
						const { guess } = ROUNDS_INFO[i];

						const locationMarker = L.marker([location.lat, location.lng]);
						const guessMarker    = L.marker([guess.lat, guess.lng]);
						const line           = getDistanceLine(location, guess);

						locationMarker.bindTooltip(`Round ${i+1}`, TOOLTIP_OPTIONS).openTooltip();

						MAP_ELEMENTS.allGuesses.addLayer(locationMarker);
						MAP_ELEMENTS.allGuesses.addLayer(guessMarker);
						MAP_ELEMENTS.allGuesses.addLayer(line);
					}

					MAP_ELEMENTS.allGuesses.addTo(MAP_OBJECT);

					MAP_OBJECT.fitBounds(MAP_ELEMENTS.allGuesses.getBounds());
				}

