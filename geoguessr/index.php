					hideEl(ROUND_RESULTS);

					generateResults();

					showAllGuesses();

					showEl(GAME_RESULTS);
				}

				function generateResults() {
					const resultsHtml = [];

					// Round results
					for (let i = 0; i < ROUNDS_INFO.length; i++) {
						resultsHtml.push(`
							<div class="round-info">
								<h3>Round ${i+1}</h3>
								<p class="round-score-and-guess">${ROUNDS_INFO[i].score}/10 - ${ROUNDS_INFO[i].distance.toLocaleString()} km</p>
