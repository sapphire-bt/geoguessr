				function showGuessDistance() {
					MAP_ELEMENTS.distanceLine = getDistanceLine(LOCATIONS[CURRENT_ROUND], MAP_ELEMENTS.guessMarker.getLatLng());

					MAP_ELEMENTS.distanceLine.addTo(MAP_OBJECT);

					// Zoom the map to the markers/line
					MAP_OBJECT.fitBounds(MAP_ELEMENTS.distanceLine.getBounds());

					MAP_ELEMENTS.guessMarker.bindTooltip("Your guess", TOOLTIP_OPTIONS).openTooltip();
				}

				// Calculate score based on two sets of coordinates
				function scoreGuess() {
					if (MAP_ELEMENTS.guessMarker === null) {
						alert("ERROR: no marker placed");
					} else {
						const distance = distanceInKm(MAP_ELEMENTS.guessMarker.getLatLng(), LOCATIONS[CURRENT_ROUND]);

						let score = 0;

						if (distance < SCORE_MAX_DISTANCE) {
							score = 10 - Math.floor((distance / SCORE_MAX_DISTANCE) * 10);
						}

						DISTANCE_AMT.textContent = distance.toLocaleString();
						SCORE_TEXT.textContent   = score;

						// Save round info in global array
						ROUNDS_INFO[CURRENT_ROUND] = {
							guess    : MAP_ELEMENTS.guessMarker.getLatLng(),
							distance : distance,
							score    : score,
							time     : Date.now() - ROUND_BEGIN,
						}
					}
				}

				function placeGuessMarker(e) {
					if (!hasGuessed()) {
						if (MAP_ELEMENTS.guessMarker === null) {
							MAP_ELEMENTS.guessMarker = L.marker([e.latlng.lat, e.latlng.lng]).addTo(MAP_OBJECT);
							showEl(GUESS_BUTTON);
						} else {
							MAP_ELEMENTS.guessMarker.setLatLng(e.latlng);
						}
					}
				}

				function getDistanceLine(a, b) {
					const latlngs = [
						[a.lat, a.lng],
						[b.lat, b.lng],
					];

					const lineOptions = {
						color: "red",
					}

					return L.polyline(latlngs, lineOptions);
				}

