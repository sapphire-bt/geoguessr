				MAP_OBJECT.on("click", placeGuessMarker);

				// Begin round when clicking "Next Round"
				NEXT_ROUND.addEventListener("click", beginRound);

				// End round when clicking "Make Guess"
				GUESS_BUTTON.addEventListener("click", endRound);

				// Show game total results after final round results
